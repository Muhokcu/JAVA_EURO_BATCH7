package gunler.gun19_16_11_2022;

public class NestedLoop {
    public static void main(String[] args) {
        for (int i = 1; i <= 5; i++) {
            System.out.println("Dis dongu : " + i);
            for (int j = 1; j <= 5; j++) {
                System.out.println("Ic Dongu :  " + j);
            }
        }
    }
}
