package gunler.gun17_14_11_2022;

public class DoWhile2 {
    public static void main(String[] args) {
        int i = 5;
        do {
            System.out.println("Hello");
            System.out.println(i);
            i--;
        } while (i > 0);
        System.out.println("i = " + i);

    }
}
