package gunler.gun04_21_10_2022;

public class BackSlash {
    public static void main(String[] args) {
        System.out.println(12345678);
        System.out.println("ali ata bak");
        System.out.println("12345678");
        System.out.println("Ispanya\tItalya\bNorvec\nIsvec");
        //                          t bosluk b son harf gidiyo n asagi satira gidiyor
        System.out.println("Ali \"merhaba\"dedi");
        System.out.println("Istanbul");
        System.out.println("\"Merhaba\" = " + "Merhaba");
    }
}
