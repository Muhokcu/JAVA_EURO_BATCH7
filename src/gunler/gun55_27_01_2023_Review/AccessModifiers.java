package gunler.gun55_27_01_2023_Review;

public class AccessModifiers {

    // public variable
    public int publicVar = 1;

    // protected variable
    protected int protectedVar = 2;

    // default variable
    int defaultVar = 3;

    // private variable
    private int privateVar = 4;

    // public method
    public void publicMethod(){
        System.out.println();
    }

    // protected method
    protected void protectedMpethod(){
        System.out.println();
    }

    // default method
    void defaultMethod(){
        System.out.println();
    }

    // private method
    private void privateMethod(){
        System.out.println();
    }
}